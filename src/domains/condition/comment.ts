/** 같은 날짜에는 항상 같은 멘트가 유지되도록 날짜 문자열 기반으로 선택한다. */
export function getConditionCommentByDate(dateValue: string): string {
  const seed = [...dateValue].reduce((sum, char) => sum + char.charCodeAt(0), 0);

  return CONDITION_COMMENTS[seed % CONDITION_COMMENTS.length] ?? CONDITION_COMMENTS[0];
}
